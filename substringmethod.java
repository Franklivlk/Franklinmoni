package Day4;

public class substringmethod {
	public static void main(String[] args) {
		String q = "knowledge";
		String w = q.substring(0, 8);
		System.out.println(w);
		System.out.println(q.substring(4));
		System.out.println(q.substring(0));
		System.out.println(q.charAt(4));

		String g = "sir issac newton college of a engineering and technology";
		System.out.println(g.charAt(g.length() - 1));

		System.out.println(g.charAt(g.length() / 2));

		System.out.println(g.charAt(g.length() - 6));

	}

}

































































